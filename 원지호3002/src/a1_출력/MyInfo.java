package a1_출력;

public class MyInfo {

	public static void main(String[] args) {
		int studentYear = 2;
		// studentYear (4바이트 정수형)변수에 1이라는 값을 대입한다.
		
		System.out.print("이름: ");
		System.out.println("원지호");
		System.out.print("연락처: ");
		System.out.println("010-5838-3002");
		System.out.print("주소: ");
		System.out.println("부산시 영도구");
		System.out.print("학교: ");
		System.out.println("동의과학대학교");
		System.out.print("전공: ");
		System.out.println("자동차");
		System.out.print("학년: ");
		System.out.println(studentYear);
		System.out.println("");
		
		
		// 연락처 : 010-5838-3002
		// 주소 : 부산 영도구
		// 학교 : 동의과학대학교
		// 전공 : 자동차
	}

}
